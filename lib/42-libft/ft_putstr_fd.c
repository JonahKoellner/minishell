#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		return (ft_putstr_fd("(null)", 1));
	while (s && *s)
	{
		ft_putchar_fd(*s++, fd);
		i++;
	}
	return (i);
}
